def compute_lap_time_performance(group):
    results = []
    for i in range(len(group)):
        if i == 0:
            results.append(0.0)
        else:
            avg_previous = group.iloc[:i].mean()
            current = group.iloc[i]
            if avg_previous > 0:
                results.append((current - avg_previous) / avg_previous)
            else:
                results.append(0.0)
    return results

