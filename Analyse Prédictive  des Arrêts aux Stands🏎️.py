def safe_rolling_mean(group, window):
    values = group['milliseconds'].values
    results = []
    for i in range(len(values)):
        if i == 0:
            results.append(np.nan)
        else:
            start = max(0, i - window)
            results.append(np.mean(values[start:i]))
    return results

